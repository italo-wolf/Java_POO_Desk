    public void atualizartabela() {
           
        CNN d = new CNN();
        d.conect();//Abre a conex√£o com banco;

        try {
            ResultSet rs = d.stm.executeQuery("SELECT * FROM cliente");//Cria a sintax de select no banco;//Executa a sintax de insert dentro do banco;

            DefaultTableModel modelo
                    = (DefaultTableModel) Tabela.getModel();

            int rows = modelo.getRowCount();

            for (int i = rows - 1; i >= 0; i--) {
                modelo.removeRow(i);
            }

            while (rs.next()) {
                int id = rs.getInt("id");
                String nome_cli = rs.getString("nome_cli");
                String telefone = rs.getString("telefone");
                String email = rs.getString("email");

                modelo.addRow(new Object[]{
                    id,
                    nome_cli,
                    telefone,
                    email
                });

            }
        } catch (SQLException ex) {
            Logger.getLogger(Janlea_Cadastra_Cli.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

